import SwiftUI

struct FrameworkTitleView: View {
    let framework: Framework
    
    var body: some View {
        VStack {
            Image(framework.imageName)
                .resizable()
                .frame(width: 90, height: 90)
            Text(framework.name)
                .font(.title2)
                .fontWeight(.semibold)
                .foregroundStyle(Color(.label))
                .scaledToFit() // A view scales to fit its parent, maintaining this viewâ€™s aspect ratio.
                .minimumScaleFactor(0.5)
        }
        .padding()
    }
}

